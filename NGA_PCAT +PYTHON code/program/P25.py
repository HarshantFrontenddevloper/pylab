# Tuple Example 
person  = ('Jhon  Doe', 30 , 'Male', 'john.doe@gmail.com')

# Accessing  elements of a tuple
print(person[0]) 
print(person[1]) 
print(person[2]) 
print(person[3]) 


#Tuple unpacking 
name , age , gender, email = person

print(name)
print(age)
print(gender)
print(email)


print(person)
print(type(person))
